int InicializaMelodia (TipoMelodia *melodia, char *nombre, int *array_frecuencias, int *array_duraciones, int num_notas) {
	int i=0;

	strcpy(melodia->nombre, nombre);

	for(i=0; i< MAX_NUM_NOTAS ; i++){

			melodia->frecuencias[i]=array_frecuencias[i];
	}
	for(i=0; i< MAX_NUM_NOTAS ; i++){
			melodia->duraciones[i]=array_duraciones[i];
		}
	melodia->num_notas=num_notas;

	return melodia->num_notas;
}

void fsm_setup(fsm_t* music_fsm) {
	piLock (FLAGS_KEY);
	flags = 0;
	piUnlock (FLAGS_KEY);
	piLock (STD_IO_BUFFER_KEY);
	piUnlock (STD_IO_BUFFER_KEY);
}

int systemSetup (void) {
	wiringPiSetupGpio();
	softToneCreate(GPIO_PIN);
	return 0;
}

void delay_until (unsigned int next) {
	unsigned int now = millis();

	if (next > now) {
		delay (next - now);
    }
}

void pulsaciones(){
	TipoSistema sistema;
	while (1) {
		delay(10); // Wiring Pi function: pauses program execution for at least 10 ms

		piLock (STD_IO_BUFFER_KEY);

		if(kbhit()) { // Funcion que detecta si se ha producido pulsacion de tecla alguna
			sistema.teclaPulsada = kbread(); // Funcion que devuelve la tecla pulsada

			printf("\n[KBHIT][%c]\n", sistema.teclaPulsada);

				// Si estamos jugando...
				switch(sistema.teclaPulsada) {
					case 's':
						piLock (FLAGS_KEY);
						flags |= FLAG_PLAYER_START;
						piUnlock (FLAGS_KEY);
						break;

					case 't':
						piLock (FLAGS_KEY);
						flags |= FLAG_PLAYER_END;
						piUnlock (FLAGS_KEY);
						break;

					case 'q':
						printf("\n Quitamos la melodia \n");
						exit(0);
						break;

					default:
						printf("INVALID KEY!!!\n");
						break;
				}
		}
		piUnlock(STD_IO_BUFFER_KEY);
	}
}

int main (){
	TipoSistema sistema;
	TipoMelodia melodia;
	sistema.player.melodia = &melodia;
	tmr_t* aux_tmr = tmr_new(pulsaciones);
	sistema.player.myTimer = (aux_tmr);

	unsigned int next;

	fsm_t* aux_fsm = fsm_new(WAIT_START, transition_table, &(sistema.player));
	systemSetup();
	InicializaMelodia(sistema.player.melodia, "GOT", frecuenciaGOT, tiempoGOT, 518);
	fsm_setup(aux_fsm);

	next = millis();
	while (1) {
		flags = 0x01;
		fsm_fire (aux_fsm);
		next += CLK_FMS;
		delay_until (next);
	}
	fsm_destroy (aux_fsm);
	tmr_destroy(aux_tmr);
}
